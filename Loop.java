public class Loop
{
public static void main(String[] args)
{
int a=5;
while(a<10)                                      //while condition
{
System.out.println(a);
a++;
}

do                                              //do while condition
{
System.out.println(a);
a++;
}
while(a<10);

for(int y=2;y<=10;y++)
{
System.out.println(y);
}
}
}
