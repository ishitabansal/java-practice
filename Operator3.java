class Operator3
{
  public static void main(String[] args)
  {
   int x=20;                  //declare variables
   int y=-20;
   boolean a=true;           //boolean operators
   boolean b=false;
   System.out.println(~x);
   System.out.println(~y);
   System.out.println(!a);
   System.out.println(!b);
  }
}
