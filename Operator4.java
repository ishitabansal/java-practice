class Operator4
{
  public static void main(String[] args)
  {
   int x=40;                    //declare two variable
   int y=25;
   System.out.println(x+y);     //making a calculator with two variables
   System.out.println(x-y);
   System.out.println(x*y);
   System.out.println(x/y);
   System.out.println(x%y);
  }
}
