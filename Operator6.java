class Operator6
{
  public static void main(String[] args)
  {
    int a=9;                           //declaring the variables
    int b=6;
    int c=20;
    System.out.println(a<b&&a++<c);   //logical and bitwise operator
    System.out.println(a);
    System.out.println(a<b&a++<c);
    System.out.println(a);
   }
}
